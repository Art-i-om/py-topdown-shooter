class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.get_map()

    def get_map(self):
        for j, row in enumerate(mini_map):
            for i, tile in enumerate(row):
                if tile > 0:
                    self.world_map[(i, j)] = tile
